#include <iostream>
using namespace std;

int main()
{
    int a,b,sum,pro;
    cout << " enter any two numbers to find sum  and product \n" <<endl;
    cin >> a >>b ;

    sum=a+b ;
    pro=a*b ;
    cout << " sum of " << a << "and" <<b<<"is=" <<sum <<endl;
    cout << " product of " <<a<< "and" <<b<< "is=" <<pro <<endl;
    return 0;
}